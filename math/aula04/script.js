/*
    Switch Case - Controlador de Fluxo
*/

const temp = 50

switch (temp) {

        case 10 :
            console.log("Ta meio frio")
        break

        case 20 :
                console.log("Ta meio quente")
        break

        case 30:
                console.log("Aqui ta quente")
        break

    default:
            console.log("Nao sei te dizer")
        break
}